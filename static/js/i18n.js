function applyLanguage(lang) {
  const t = translations[lang] || translations['en'];

  // Text elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // Placeholder elements
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // Sync language selector if present
  const sel = document.getElementById('langSelect');
  if (sel) sel.value = lang;
}

function setLanguage(lang) {
  localStorage.setItem('language', lang);
  applyLanguage(lang);
}

// --- Dark Mode (global, works on all pages) ---
function applyDarkMode(enabled) {
  if (enabled) {
    document.documentElement.classList.add('dark');
  } else {
    document.documentElement.classList.remove('dark');
  }
}

function toggleDarkModeGlobal() {
  const next = !(localStorage.getItem('darkMode') === 'true');
  localStorage.setItem('darkMode', next);
  applyDarkMode(next);
  return next;
}

// Auto-apply on every page load
document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('language') || 'en';
  applyLanguage(savedLang);

  const darkEnabled = localStorage.getItem('darkMode') === 'true';
  applyDarkMode(darkEnabled);
});
